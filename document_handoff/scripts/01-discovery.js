export const meta = {
  name: 'handoff-discovery',
  description: 'Scan source directory and produce file catalog HTML',
  phases: [
    { title: 'Scan', detail: 'Parallel directory scanners' },
    { title: 'Catalog', detail: 'Write catalog HTML' },
  ],
}

const _args = typeof args === 'string' ? JSON.parse(args) : args
const { project, source_root, output_dir } = _args

const SCAN_TARGETS = [
  { key: 'root',       path: source_root,                  depth_note: 'top-level files only (not recursive)', bucket_rules: 'ext .jsonl → "logs"; ext .xlsx or .pdf or .docx or .csv or .xls or .pptx or .xlsm → "inputs"; name matches plan*.md or starts with TODO or TASK (case-insensitive) → "plans"; name is MEMORY.md or matches memory*.md (case-insensitive) → "memory"; everything else → "artifacts"' },
  { key: 'code',       path: `${source_root}\\code`,       depth_note: 'recursive', default_bucket: 'code' },
  { key: 'logs',       path: `${source_root}\\logs`,       depth_note: 'recursive', default_bucket: 'logs' },
  { key: 'artifacts',  path: `${source_root}\\artifacts`,  depth_note: 'recursive', default_bucket: 'artifacts' },
  { key: 'plans',      path: `${source_root}\\plans`,      depth_note: 'recursive', default_bucket: 'plans' },
  { key: 'memory',     path: `${source_root}\\memory`,     depth_note: 'recursive', default_bucket: 'memory' },
  { key: 'inputs',     path: `${source_root}\\inputs`,     depth_note: 'recursive', default_bucket: 'inputs' },
  { key: 'deprecated', path: `${source_root}\\deprecated`, depth_note: 'recursive', default_bucket: 'deprecated' },
]

const DIR_SCHEMA = {
  type: 'object',
  required: ['key', 'path', 'exists', 'file_count', 'total_bytes', 'files'],
  properties: {
    key:         { type: 'string' },
    path:        { type: 'string' },
    exists:      { type: 'boolean' },
    file_count:  { type: 'number' },
    total_bytes: { type: 'number' },
    files: {
      type: 'array',
      items: {
        type: 'object',
        required: ['name', 'rel_path', 'size_bytes', 'ext', 'description', 'bucket'],
        properties: {
          name:        { type: 'string' },
          rel_path:    { type: 'string' },
          size_bytes:  { type: 'number' },
          ext:         { type: 'string' },
          description: { type: 'string' },
          bucket:      { type: 'string' },
        }
      }
    }
  }
}

phase('Scan')

const scanResults = await parallel(SCAN_TARGETS.map(t => () =>
  agent(
    `Scan directory for the document-handoff catalog.

Target: ${t.path}
Scan type: ${t.depth_note}
Skip any ".handoff" subdirectory.

If the directory does not exist:
Return: { "key": "${t.key}", "path": "${t.path}", "exists": false, "file_count": 0, "total_bytes": 0, "files": [] }

If it exists, list all files matching the scan type. For each file:
- name: filename only (no path)
- rel_path: path relative to ${source_root} using backslashes
- size_bytes: exact file size in bytes (use Get-ChildItem or equivalent)
- ext: lowercase extension with dot (e.g. ".py", ".jsonl", ".md", "" if none)
- description: one-line description based on filename/extension
- bucket: workfolder destination — ${t.default_bucket ? `"${t.default_bucket}" for all files in this directory` : `classify per file using these rules: ${t.bucket_rules}`}

Return the complete structured inventory.`,
    { label: `scan:${t.key}`, schema: DIR_SCHEMA }
  )
))

const valid = scanResults.filter(Boolean)
const totalFiles = valid.reduce((s, r) => s + r.file_count, 0)
const totalBytes = valid.reduce((s, r) => s + r.total_bytes, 0)
const totalMB = (totalBytes / 1024 / 1024).toFixed(1)

phase('Catalog')

await agent(
  `Write a self-contained HTML file catalog.

Output file: ${output_dir}\\${project}-catalog.html

Directory inventory (JSON):
${JSON.stringify(valid, null, 2)}

Requirements:
- DOCTYPE html, UTF-8 charset
- Embedded <style> block — dark mode: background #1a1a2e, surface #16213e, text #e2e8f0, border #0f3460, accent #4ade80
- Page header: "Project: ${project}" | "Files: ${totalFiles}" | "Size: ${totalMB} MB"
- Summary table: columns = Directory | Files | Size | Status. One row per directory. Status = green badge "Present" or gray badge "Empty" or red badge "Not Found".
- Detail section per existing, non-empty directory: h2 with dir name, then table with columns Name | Relative Path | Size | Type | Description
- Footer: "Generated by document-handoff skill"
- Write the complete HTML to the output file path above.`,
  { label: 'write-catalog' }
)

return {
  file_inventory: valid,
  catalog_path:   `${output_dir}\\${project}-catalog.html`,
  total_files:    totalFiles,
  total_bytes:    totalBytes,
}
