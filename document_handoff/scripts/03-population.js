export const meta = {
  name: 'handoff-population',
  description: 'Copy project files into workfolder capsule; write inputs/REFERENCES.md for baseline inputs',
  phases: [
    { title: 'Copy',      detail: 'Copy files by bucket into workfolder subdirectories' },
    { title: 'Reference', detail: 'Write inputs/REFERENCES.md for baseline input files' },
  ],
}

const _args = typeof args === 'string' ? JSON.parse(args) : args
const { project, source_root, output_dir, state } = _args
const { file_inventory } = state

const BUCKET_SUBDIR = {
  plans:      'plans',
  memory:     'memory',
  code:       'code\\src',
  artifacts:  'artifacts',
  deprecated: 'deprecated',
}

const allFiles = (file_inventory || []).flatMap(d => d.files || [])

const inputFiles = allFiles.filter(f => f.bucket === 'inputs')
const copyFiles  = allFiles.filter(f => f.bucket !== 'inputs')

function getSubdir(f) {
  if (f.bucket === 'logs') {
    return f.rel_path.toLowerCase().includes('codex') ? 'logs\\codex' : 'logs\\claude'
  }
  return BUCKET_SUBDIR[f.bucket] || 'artifacts'
}

// Group by destination subdir for parallel copy operations
const bySubdir = {}
for (const f of copyFiles) {
  const subdir = getSubdir(f)
  if (!bySubdir[subdir]) bySubdir[subdir] = []
  bySubdir[subdir].push(f)
}

phase('Copy')

if (Object.keys(bySubdir).length > 0) {
  await parallel(Object.entries(bySubdir).map(([subdir, files]) => () =>
    agent(
      `Copy files into the handoff workfolder capsule.

Destination directory: ${output_dir}\\${subdir}

Step 1 — ensure destination exists:
New-Item -ItemType Directory -Path "${output_dir}\\${subdir}" -Force | Out-Null

Step 2 — copy each file:
${files.map(f =>
  `Copy-Item -Path "${source_root}\\${f.rel_path}" -Destination "${output_dir}\\${subdir}\\${f.name}" -Force`
).join('\n')}

Report how many files succeeded and list any that failed.`,
      { label: `copy:${subdir.replace('\\', '-')}` }
    )
  ))
}

log(`Copied ${copyFiles.length} files into workfolder. ${inputFiles.length} input files will be referenced only.`)

phase('Reference')

const refTableRows = inputFiles.length > 0
  ? inputFiles.map(f =>
      `| ${f.name} | ${source_root}\\${f.rel_path} | ${(f.size_bytes / 1024).toFixed(1)} KB | ${f.description || '—'} |`
    ).join('\n')
  : '| — | No input files identified | — | — |'

await agent(
  `Write this exact content to file: ${output_dir}\\inputs\\REFERENCES.md

# Baseline Input References

These files are the original source inputs for this project. They were **not copied** into the capsule — access them at their original locations.

| File | Original Path | Size | Description |
|------|---------------|------|-------------|
${refTableRows}

---
*Generated by document-handoff population phase*`,
  { label: 'write-references' }
)

return {
  files_copied:     copyFiles.length,
  files_referenced: inputFiles.length,
  references_path:  `${output_dir}\\inputs\\REFERENCES.md`,
}
